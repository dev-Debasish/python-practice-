class Car:
    brand = "BMW"
    model = "M5 competition"
    
myCar = Car()    # this parenthesis automatically calls the constractor 

print(myCar)

#  by default the all Car class objects attributes would be same, as it is defined
print(myCar.brand)
print(myCar.model)


#??   An instance refers specifically to an object that is created from a class. When you define a class and create an object from it, that object is called an instance of the class.


